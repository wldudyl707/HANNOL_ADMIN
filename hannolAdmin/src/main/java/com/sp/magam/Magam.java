package com.sp.magam;

public class Magam {
	private long usersCode;
	private int listNum;
	private int year;
	private int month;
	private String magamDate;
	private String name;
	private int isMagam;

	public int getIsMagam() {
		return isMagam;
	}

	public void setIsMagam(int isMagam) {
		this.isMagam = isMagam;
	}

	public long getUsersCode() {
		return usersCode;
	}

	public void setUsersCode(long usersCode) {
		this.usersCode = usersCode;
	}

	public int getListNum() {
		return listNum;
	}

	public void setListNum(int listNum) {
		this.listNum = listNum;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public String getMagamDate() {
		return magamDate;
	}

	public void setMagamDate(String magamDate) {
		this.magamDate = magamDate;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
